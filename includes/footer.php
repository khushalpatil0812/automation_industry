<script>
    // AOS init (keeps original options)
    if (typeof AOS !== 'undefined') {
        AOS.init({ duration: 800, easing: 'ease-in-out', once: true, offset: 50 });
    }

    // Set current year
    (function(){
        var y = new Date().getFullYear();
        var el = document.getElementById('current-year');
        if(el) el.textContent = y;
    })();

    // Back-to-top behavior: show on scroll, smooth scroll, keyboard accessible
    (function(){
        var btn = document.querySelector('.back-to-top');
        if(!btn) return;
        var showAfter = 300;
        function onScroll(){
            if(window.scrollY > showAfter){ btn.style.opacity = '1'; btn.style.transform = 'translateY(0)'; btn.setAttribute('aria-hidden','false'); }
            else { btn.style.opacity = '0'; btn.style.transform = 'translateY(10px)'; btn.setAttribute('aria-hidden','true'); }
        }
        window.addEventListener('scroll', onScroll, {passive:true});
        btn.addEventListener('click', function(){ window.scrollTo({top:0, behavior:'smooth'}); btn.blur(); });
        btn.addEventListener('keydown', function(e){ if(e.key === 'Enter' || e.key === ' ') { e.preventDefault(); window.scrollTo({top:0, behavior:'smooth'}); } });
        // initial state
        onScroll();
    })();
</script>

